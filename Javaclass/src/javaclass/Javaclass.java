public class Javaclass{
	public static void main(String[]text){
boolean b=true;
b=false;
System.out.println(b);//若要印出b=false (左邊b=是固定的 可以用字串 false 是變數)
System.out.print("b=");
System.out.print(b);
char c = 'A';
System.out.println(c);
c = '@';
System.out.println(c);
c = '我';
System.out.println(c);
c = '\'';//使用單引號 印出'
System.out.println(c);
c = '\\';//使用單引號 印出\
System.out.println(c);
c = '\u0811';//使用單引號 印出你要的萬國碼 Unicode
System.out.println(c);
String s ="Hello Java";
System.out.println(s);
	}}

